def generate_report(output_path=None):
    config = load_config()
    combos = config["combos"]
    genes = config["genes_unique"]

    print("Loading data from all sources...")
    all_data = load_all_data(
        genes=genes,
        combos=combos,
        base_dir=str(BASE_DIR),
        indication="ibd",
    )

    print("Calculating scores...")
    scores = calculate_overall_scores(
        all_data=all_data,
        combos=combos,
        genes=genes,
        gene_approaches=GENE_APPROACHES,
    )
    combo_scores = scores["combos"]
    gene_scores = scores["genes"]

    print("Extracting Plotly bundle from target_signature exports...")
    plotly_bundle = extract_plotly_bundle()
