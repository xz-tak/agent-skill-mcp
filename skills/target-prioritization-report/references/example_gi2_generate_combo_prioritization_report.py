def markdown_to_html(md_text):
    if not md_text:
        return ""
    html = md_text
    html = re.sub(r"^### (.+)$", r"<h4>\1</h4>", html, flags=re.MULTILINE)
    html = re.sub(r"^## (.+)$", r"<h3>\1</h3>", html, flags=re.MULTILINE)
    html = re.sub(r"^# (.+)$", r"<h3>\1</h3>", html, flags=re.MULTILINE)
    html = re.sub(r"\*\*(.+?)\*\*", r"<strong>\1</strong>", html)
    html = re.sub(r"\*(.+?)\*", r"<em>\1</em>", html)
    html = re.sub(r"^- (.+)$", r"<li>\1</li>", html, flags=re.MULTILINE)
    html = re.sub(r"(<li>.*?</li>(\s*<li>.*?</li>)*)", r"<ul>\1</ul>", html, flags=re.DOTALL)
    out = []
    for line in html.split("\n"):
        line = line.strip()
        if not line:
            continue
        if line.startswith("<"):
            out.append(line)
        else:
            out.append(f"<p>{line}</p>")
    return "\n".join(out)


def extract_narrative_section(md_content, target_slug):
    if not md_content:
        return "<p>No narrative available.</p>"
    target_genes = target_slug.split("_")
    lines = md_content.split("\n")
    relevant = []
    capturing = False
    for line in lines:
        if any(g in line for g in target_genes) and (line.startswith("#") or line.startswith("**")):
            capturing = True
            relevant.append(line)
        elif capturing:
            if line.startswith("#") and not any(g in line for g in target_genes):
                break
            relevant.append(line)
    if relevant:
        return markdown_to_html("\n".join(relevant[:30]))
    return markdown_to_html("\n".join(lines[:20]))


def load_pathway_narrative(combo, results_dir):
    pathway_dir = results_dir / "pathwaydb_ibd"
    if not pathway_dir.exists():
        return "<p>Pathway data not available.</p>"
    for md_file in pathway_dir.glob("*summary*.md"):
        content = md_file.read_text(errors="ignore")
        return extract_narrative_section(content, combo_slug(combo))
    return "<p>No pathway summary found.</p>"


