def build_radar_figure(combo_scores):
    categories = ["Clinical", "Biology", "Safety", "Druggability", "Translation", "Commercial"]
    top = sorted(combo_scores.items(), key=lambda kv: kv[1].get("overall", 0), reverse=True)
    traces = []
    for name, scores in top:
        values = [
            scores.get("clinical", 0),
            scores.get("biology", 0),
            scores.get("safety", 0),
            scores.get("druggability", 0),
            scores.get("translation", 0),
            scores.get("commercial", 0),
        ]
        values.append(values[0])
        traces.append({
            "type": "scatterpolar",
            "r": values,
            "theta": categories + [categories[0]],
            "fill": "toself",
            "name": name,
            "opacity": 0.7,
        })
    layout = {
        "polar": {"radialaxis": {"visible": True, "range": [0, 100]}},
        "showlegend": True,
        "title": "Combination Subscore Radar",
        "height": 520,
        "margin": {"t": 60, "b": 40, "l": 60, "r": 60},
    }
    return {"data": traces, "layout": layout, "config": {"responsive": True}}


def build_translation_html(combo, combo_data, gene_scores, bio_components):
    """Build Translation section HTML for a combo card."""
    genes = combo
    ukbppp_score = bio_components.get("ukbppp", 0.0)
    sig_score = bio_components.get("signature", 0.0)
    translation_score = 0.5 * ukbppp_score + 0.5 * sig_score

    html = '<div class="bar-plot" aria-label="Translation component bars">'
    html += f'<div class="bar" style="--value: {ukbppp_score};"><div class="row"><div><div class="name">UKBPPP</div><div class="meta">weight 0.50</div></div><div class="track" aria-hidden="true"><div class="fill"></div></div><div class="meta" style="text-align:right;"><span class="score">{ukbppp_score:.1f}</span> / 100</div></div></div>'
    html += f'<div class="bar" style="--value: {sig_score};"><div class="row"><div><div class="name">Combo Signature</div><div class="meta">weight 0.50</div></div><div class="track" aria-hidden="true"><div class="fill"></div></div><div class="meta" style="text-align:right;"><span class="score">{sig_score:.1f}</span> / 100</div></div></div>'
    html += '</div>'

    html += '<details open style="margin-top: 14px;"><summary>Per-gene UKBPPP scores</summary>'
    html += '<table class="rank-table score-color-table" style="margin-top: 10px;"><thead><tr><th>Gene</th><th>UKBPPP Score</th></tr></thead><tbody>'
    for g in genes:
        gs = gene_scores.get(g, {})
        gc = gs.get("components", {}).get("biology", {})
        ukb = gc.get("ukbppp", 0.0)
        html += f'<tr><td style="font-weight:850;">{g}</td><td class="scorecell">{ukb:.1f}</td></tr>'
