def extract_plotly_bundle():
    """Pull the inlined plotly.js bundle out of a target_signature HTML.

    Scans ``test/omicsoft_full/*/target_signature_*.html`` and returns the
    first script block longer than 100 KB that contains the "plotly.js"
    banner. Returns an empty-bundle comment if nothing usable is found.
    """
    candidates = sorted(OMICSOFT_DIR.glob("*/target_signature_*.html"))
    for html_path in candidates:
        try:
            content = html_path.read_text(errors="ignore")
        except Exception:
            continue
        for match in re.finditer(r"<script\b[^>]*>(.*?)</script>", content, re.DOTALL):
            body = match.group(1)
            if len(body) > 100_000 and "plotly.js" in body[:2000].lower():
                return body
    return "/* Plotly bundle not found — interactive charts will not render. */"


def _parse_balanced(s, start, open_ch, close_ch):
    """Return (end_idx_exclusive, parsed_value) for the JSON expression that
    begins at ``s[start]``. Handles nested brackets and string-aware bracket
    counting. Returns ``(start, None)`` on failure.
    """
    if start >= len(s) or s[start] != open_ch:
        return start, None
    depth = 0
    i = start
    in_str = False
    escape = False
    n = len(s)
    while i < n:
        ch = s[i]
        if in_str:
            if escape:
                escape = False
            elif ch == "\\":
                escape = True
            elif ch == '"':
                in_str = False
        else:
            if ch == '"':
                in_str = True
            elif ch == open_ch:
                depth += 1
            elif ch == close_ch:
                depth -= 1
                if depth == 0:
                    end = i + 1
                    try:
                        return end, json.loads(s[start:end])
                    except json.JSONDecodeError:
                        return start, None
        i += 1
    return start, None


def extract_plotly_figure(html_path):
    """Extract ``data`` / ``layout`` / ``config`` from a Plotly HTML export.

    Locates a ``Plotly.newPlot("<div_id>", [data], {layout}, {config})`` call
    that creates a real chart (skipping incidental occurrences inside the
    plotly bundle itself) and JSON-decodes its arguments.
    """
    try:
        content = Path(html_path).read_text(errors="ignore")
    except Exception:
        return None

    # Walk every occurrence of `Plotly.newPlot(` and try to parse one whose
    # first non-whitespace argument is a quoted div-id string.
    pat = re.compile(r"Plotly\.newPlot\(\s*\"")
    for m in pat.finditer(content):
        # Position of opening quote of the div-id.
        i = m.end() - 1  # last char in match is the opening quote
        # Find matching closing quote (no internal escapes expected for uuids).
        end_quote = content.find('"', i + 1)
        if end_quote == -1:
            continue
        # Walk past comma + whitespace to the data array.
        j = end_quote + 1
        while j < len(content) and content[j] in " ,\t\n\r":
            j += 1
        if j >= len(content) or content[j] != "[":
            continue
        data_end, data = _parse_balanced(content, j, "[", "]")
        if data is None:
            continue
        k = data_end
        while k < len(content) and content[k] in " ,\t\n\r":
            k += 1
        if k >= len(content) or content[k] != "{":
            continue
        layout_end, layout = _parse_balanced(content, k, "{", "}")
        if layout is None:
            continue
        # Optional config.
        config = {"responsive": True}
        m2 = layout_end
        while m2 < len(content) and content[m2] in " \t\n\r":
            m2 += 1
        if m2 < len(content) and content[m2] == ",":
            m2 += 1
            while m2 < len(content) and content[m2] in " \t\n\r":
                m2 += 1
            if m2 < len(content) and content[m2] == "{":
                _, cfg = _parse_balanced(content, m2, "{", "}")
                if cfg is not None:
                    config = cfg
        return {"data": data, "layout": layout, "config": config}
    return None






def build_persample_figures(combo):
    """Extract per-sample target_expression plots as Plotly figure dicts.

    Returns a list of (key, fig) pairs keyed as ``<study>::<plot_stem>``. The
    per-sample HTML exports are small enough (~5 MB, mostly the plotly bundle)
    that the extracted figure JSON is only ~40 KB each, so embedding them in
    PLOT_FIGS keeps the report compact and reuses the single inlined plotly
    bundle.
    """
    combo_dir = find_combo_dir(combo)
    if combo_dir is None:
        return []
    sample_root = combo_dir / "sample_level"
    if not sample_root.is_dir():
        return []

    out = []
    for study_dir in sorted(p for p in sample_root.iterdir() if p.is_dir()):
        for html_file in sorted(study_dir.glob("*.html")):
            fig = extract_plotly_figure(html_file)
            if fig is None:
                continue
            key = f"{study_dir.name}::{html_file.stem}"
            out.append((key, fig))
    return out


def build_archs4_figures(combo):
    """Extract ARCHS4 tissue expression plots as Plotly figure dicts."""
    slug = combo_slug(combo)
    archs4_dir = BASE_DIR / "test" / "archs4"
    out = []
    for plot_type in ["boxplot", "heatmap"]:
        html_path = archs4_dir / f"{slug}_{plot_type}.html"
        if html_path.exists():
            fig = extract_plotly_figure(html_path)
            if fig is not None:
                key = f"{plot_type}"
                out.append((key, fig))
    return out


def build_cellxgene_figures(combo):
    """Extract cellxgene per-celltype coexpression heatmaps as Plotly figure dicts."""
    slug = combo_slug(combo)
    cellxgene_dir = BASE_DIR / "test" / "cellxgene"
    if not cellxgene_dir.is_dir():
        return []
    out = []
    for celltype_dir in sorted(p for p in cellxgene_dir.iterdir() if p.is_dir()):
        celltype = celltype_dir.name
        html_path = celltype_dir / f"{celltype}_{slug}_heatmap.html"
        if html_path.exists():
            fig = extract_plotly_figure(html_path)
            if fig is not None:
                out.append((celltype, fig))
    return out


