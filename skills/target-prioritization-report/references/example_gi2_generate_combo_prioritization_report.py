def collect_combo_level_figs(combo):
    """Extract combo-level signature/GSEA plots as Plotly figure dicts."""
    combo_dir = find_combo_dir(combo)
    if combo_dir is None:
        return {}
    anchor = combo_anchor(combo)
    figs = {}
    for plot_name in COMBO_LEVEL_PLOTS:
        matches = sorted(combo_dir.glob(f"{plot_name}_*.html"))
        if not matches:
            continue
        fig = extract_plotly_figure(matches[-1])  # newest by name (date suffix)
        if fig is not None:
            figs[f"{anchor}::{plot_name}"] = fig
    return figs


